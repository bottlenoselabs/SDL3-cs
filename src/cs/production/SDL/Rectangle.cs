namespace SDL;

#pragma warning disable CA1815

/// <summary>
///     Defines a rectangle.
/// </summary>
public struct Rectangle
{
    /// <summary>
    ///     The X coordinate of the rectangle.
    /// </summary>
    public int X;

    /// <summary>
    ///     The Y coordinate of the rectangle.
    /// </summary>
    public int Y;

    /// <summary>
    ///     The width of the rectangle.
    /// </summary>
    public int Width;

    /// <summary>
    ///     The height of the rectangle.
    /// </summary>
    public int Height;
}
