namespace bottlenoselabs.SDL;

/// <summary>
///     Defines the flip modes for rendering surfaces and textures.
/// </summary>
public enum FlipMode
{
    /// <summary>
    ///     No flip.
    /// </summary>
    None = 0,

    /// <summary>
    ///     Flip horizontally.
    /// </summary>
    Horizontal = 1,

    /// <summary>
    ///     Flip vertically.
    /// </summary>
    Vertical = 2
}
